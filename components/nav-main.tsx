"use client"

import { MailIcon, PlusCircleIcon, type LucideIcon } from "lucide-react"
import { usePrefetchData } from "@/hooks/use-prefetch"

import { Button } from "@/components/ui/button"
import {
  SidebarGroup,
  SidebarGroupContent,
  SidebarMenu,
  SidebarMenuButton,
  SidebarMenuItem,
} from "@/components/ui/sidebar"

export function NavMain({
  items,
}: {
  items: {
    title: string
    url: string
    icon?: LucideIcon
  }[]
}) {
  const { prefetchAll } = usePrefetchData()

  const handleMouseEnter = () => {
    // Prefetch data when hovering over product-related links
    prefetchAll()
  }

  return (
    <SidebarGroup>
      <SidebarGroupContent>
        <SidebarMenu>
          {items.map((item) => (
            <SidebarMenuItem key={item.title}>
              <SidebarMenuButton
                tooltip={item.title}
                asChild
                onMouseEnter={
                  item.url.includes('monofocales') ? handleMouseEnter : undefined
                }
              >
                <a href={item.url}>
                  {item.icon && <item.icon />}
                  <span>{item.title}</span>
                </a>
              </SidebarMenuButton>
            </SidebarMenuItem>
          ))}
        </SidebarMenu>
      </SidebarGroupContent>
    </SidebarGroup>
  )
}
